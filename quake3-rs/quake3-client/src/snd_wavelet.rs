#[no_mangle]
pub unsafe extern "C" fn MuLawEncode(mut s: libc::c_short) -> byte {
    let mut adjusted: libc::c_ulong = 0;
    let mut sign: byte = 0;
    let mut exponent: byte = 0;
    let mut mantissa: byte = 0;
    sign = (if (s as libc::c_int) < 0i32 { 0i32 } else { 0x80i32 }) as byte;
    if (s as libc::c_int) < 0i32 { s = -(s as libc::c_int) as libc::c_short }
    adjusted =
        ((s as libc::c_long) <<
             (16i32 as
                  libc::c_ulong).wrapping_sub((::std::mem::size_of::<libc::c_short>()
                                                   as
                                                   libc::c_ulong).wrapping_mul(8i32
                                                                                   as
                                                                                   libc::c_ulong)))
            as libc::c_ulong;
    adjusted = adjusted.wrapping_add((128i64 + 4i64) as libc::c_ulong);
    if adjusted > 32767i32 as libc::c_ulong {
        adjusted = 32767i32 as libc::c_ulong
    }
    exponent =
        (numBits[(adjusted >> 7i32 & 0xffi32 as libc::c_ulong) as usize] as
             libc::c_int - 1i32) as byte;
    mantissa =
        (adjusted >> exponent as libc::c_int + 3i32 & 0xfi32 as libc::c_ulong)
            as byte;
    return !(sign as libc::c_int | (exponent as libc::c_int) << 4i32 |
                 mantissa as libc::c_int) as byte;
}
