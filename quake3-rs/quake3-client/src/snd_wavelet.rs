pub unsafe extern "C" fn wt1(mut a: *mut libc::c_float, mut n: libc::c_ulong,
                             mut isign: libc::c_int) {
    let mut nn: libc::c_ulong = 0;
    let mut inverseStartLength: libc::c_int =
        n.wrapping_div(4i32 as libc::c_ulong) as libc::c_int;
    if n < inverseStartLength as libc::c_ulong { return }
    if isign >= 0i32 {
        nn = n;
        while nn >= inverseStartLength as libc::c_ulong {
            daub4(a, nn, isign);
            nn >>= 1i32
        }
    } else {
        nn = inverseStartLength as libc::c_ulong;
        while nn <= n { daub4(a, nn, isign); nn <<= 1i32 }
    };
}
