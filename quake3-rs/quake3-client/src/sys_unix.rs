pub unsafe extern "C" fn Sys_DllExtension(mut name: *const libc::c_char)
 -> qboolean {
    let mut p: *const libc::c_char = 0 as *const libc::c_char;
    let mut c: libc::c_char = 0i32 as libc::c_char;
    if 0 !=
           COM_CompareExtension(name,
                                b".so\x00" as *const u8 as
                                    *const libc::c_char) as u64 {
        return qtrue
    }
    p = strstr(name, b".so.\x00" as *const u8 as *const libc::c_char);
    if !p.is_null() {
        p =
            p.offset(strlen(b".so\x00" as *const u8 as *const libc::c_char) as
                         isize);
        while 0 != *p {
            c = *p;
            if 0 ==
                   *(*__ctype_b_loc()).offset(c as libc::c_int as isize) as
                       libc::c_int &
                       _ISdigit as libc::c_int as libc::c_ushort as
                           libc::c_int && c as libc::c_int != '.' as i32 {
                return qfalse
            }
            p = p.offset(1isize)
        }
        if c as libc::c_int != '.' as i32 { return qtrue }
    }
    return qfalse;
}
