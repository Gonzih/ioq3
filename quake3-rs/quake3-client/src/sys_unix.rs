unsafe extern "C" fn Sys_Exec() -> libc::c_int {
    let mut pid: pid_t = fork();
    if pid < 0i32 { return -1i32 }
    if 0 != pid {
        let mut exitCode: libc::c_int = 0;
        wait(&mut exitCode);
        return (exitCode & 0xff00i32) >> 8i32
    } else {
        execvp(execArgv[0usize],
               execArgv.as_mut_ptr() as *const *mut libc::c_char);
        exit(-1i32);
    };
}
