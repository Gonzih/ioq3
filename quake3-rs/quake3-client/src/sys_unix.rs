pub unsafe extern "C" fn Sys_Dialog(mut type_0: dialogType_t,
                                    mut message: *const libc::c_char,
                                    mut title: *const libc::c_char)
 -> dialogResult_t {
    let mut session: *const libc::c_char =
        getenv(b"DESKTOP_SESSION\x00" as *const u8 as *const libc::c_char);
    let mut tried: [qboolean; 4] = [qfalse, qfalse, qfalse, qfalse];
    let mut commands: [dialogCommandBuilder_t; 4] = [None, None, None, None];
    let mut preferredCommandType: dialogCommandType_t = NONE;
    let mut i: libc::c_int = 0;
    commands[ZENITY as libc::c_int as usize] = Some(Sys_ZenityCommand);
    commands[KDIALOG as libc::c_int as usize] = Some(Sys_KdialogCommand);
    commands[XMESSAGE as libc::c_int as usize] = Some(Sys_XmessageCommand);
    if 0 ==
           Q_stricmp(session,
                     b"gnome\x00" as *const u8 as *const libc::c_char) {
        preferredCommandType = ZENITY
    } else if 0 ==
                  Q_stricmp(session,
                            b"kde\x00" as *const u8 as *const libc::c_char) {
        preferredCommandType = KDIALOG
    }
    i = NONE as libc::c_int + 1i32;
    while i < NUM_DIALOG_PROGRAMS as libc::c_int {
        if !(preferredCommandType as libc::c_uint !=
                 NONE as libc::c_int as libc::c_uint &&
                 preferredCommandType as libc::c_uint != i as libc::c_uint) {
            if 0 == tried[i as usize] as u64 {
                let mut exitCode: libc::c_int = 0;
                commands[i as
                             usize].expect("non-null function pointer")(type_0,
                                                                        message,
                                                                        title);
                exitCode = Sys_Exec();
                if exitCode >= 0i32 {
                    match type_0 as libc::c_uint {
                        3 => {
                            return (if 0 != exitCode {
                                        DR_NO as libc::c_int
                                    } else { DR_YES as libc::c_int }) as
                                       dialogResult_t
                        }
                        4 => {
                            return (if 0 != exitCode {
                                        DR_CANCEL as libc::c_int
                                    } else { DR_OK as libc::c_int }) as
                                       dialogResult_t
                        }
                        _ => { return DR_OK }
                    }
                }
                tried[i as usize] = qtrue;
                if preferredCommandType as libc::c_uint !=
                       NONE as libc::c_int as libc::c_uint {
                    preferredCommandType = NONE;
                    i = NONE as libc::c_int + 1i32
                }
            }
        }
        i += 1
    }
    Com_DPrintf(b"^3WARNING: failed to show a dialog\n\x00" as *const u8 as
                    *const libc::c_char);
    return DR_OK;
}
