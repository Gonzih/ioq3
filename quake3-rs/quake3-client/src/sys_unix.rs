#[no_mangle]
pub unsafe extern "C" fn Sys_Milliseconds() -> libc::c_int {
    let mut tp: timeval = timeval{tv_sec: 0, tv_usec: 0,};
    gettimeofday(&mut tp, 0 as *mut timezone);
    if 0 == sys_timeBase {
        sys_timeBase = tp.tv_sec as libc::c_ulong;
        return (tp.tv_usec / 1000i32 as libc::c_long) as libc::c_int
    }
    curtime =
        (tp.tv_sec as
             libc::c_ulong).wrapping_sub(sys_timeBase).wrapping_mul(1000i32 as
                                                                        libc::c_ulong).wrapping_add((tp.tv_usec
                                                                                                         /
                                                                                                         1000i32
                                                                                                             as
                                                                                                             libc::c_long)
                                                                                                        as
                                                                                                        libc::c_ulong)
            as libc::c_int;
    return curtime;
}
